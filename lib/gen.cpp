void cullNeighbourTrees(int treeMap[][24]) {
    for(int i = 0;i<24;i++) {
        for(int j = 0;j<24;j++) {
            if(treeMap[i][j]==1 && treeMap[i][j+1]!=0) {
                treeMap[i][j+1] = 0;
            }
            if(treeMap[i][j]==1 && treeMap[i+1][j]!=0) {
                treeMap[i+1][j] = 0;
            }
        }
    }
}
void printTreeMap(int treeMap[][24]) {
    printf("{\n");
        for(int row = 0; row<=23;row++) {
            printf("{");
            for(int col = 0; col<=23;col++) {
                printf("%d", treeMap[row][col]);
                if(col!=23) {
                    printf(", ");
                }
            }
            printf("},\n");
        }
    printf("};");
}

